package org.webscrollers.bharath.ResumeFormat.model;

public class Skills {

	public int id;
	public String languages;
	public String framework;
	public String devtech;
	public String scripting;
	public String methodologies;
	public String servers;
	public String databases;
	public String operatingsystem;
	public String othertools;
	public int user_id;
	
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLanguages() {
		return languages;
	}
	public void setLanguages(String languages) {
		this.languages = languages;
	}
	public String getFramework() {
		return framework;
	}
	public void setFramework(String framework) {
		this.framework = framework;
	}
	public String getDevtech() {
		return devtech;
	}
	public void setDevtech(String devtech) {
		this.devtech = devtech;
	}
	public String getScripting() {
		return scripting;
	}
	public void setScripting(String scripting) {
		this.scripting = scripting;
	}
	public String getMethodologies() {
		return methodologies;
	}
	public void setMethodologies(String methodologies) {
		this.methodologies = methodologies;
	}
	public String getServers() {
		return servers;
	}
	public void setServers(String servers) {
		this.servers = servers;
	}
	public String getDatabases() {
		return databases;
	}
	public void setDatabases(String databases) {
		this.databases = databases;
	}
	public String getOperatingsystem() {
		return operatingsystem;
	}
	public void setOperatingsystem(String operatingsystem) {
		this.operatingsystem = operatingsystem;
	}
	public String getOthertools() {
		return othertools;
	}
	public void setOthertools(String othertools) {
		this.othertools = othertools;
	}
	
}
